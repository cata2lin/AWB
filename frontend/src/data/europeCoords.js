// Get coordinates for any European city
export function getEUCoordinates(cityName, countryCode) {
    // Direct lookup
    if (EU_CITIES[cityName]) {
        return EU_CITIES[cityName]
    }

    // Try normalized name (remove diacritics)
    if (cityName) {
        const normalized = cityName
            .normalize('NFD')
            .replace(/[\u0300-\u036f]/g, '')
            .trim()
        if (EU_CITIES[normalized]) {
            return EU_CITIES[normalized]
        }
    }

    return null
}
